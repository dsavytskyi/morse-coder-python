class LangPackage:
    def __init__(self, name, alphabet, IfBasePackage = False):
        self.name = name
        self.alphabet = alphabet
        self.IfBasePackage = IfBasePackage
    
    def inverse(self):
        return dict((v,k) for (k,v) in self.alphabet.items())


